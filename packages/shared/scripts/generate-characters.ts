async function fetchFlags(): Promise<string> {
    const codes: Record<string, string> = await fetch(
        'https://flagcdn.com/en/codes.json',
    ).then((r) => r.json())

    const exclude = new Set([
        'ac', 'cp', 'dg', 'ea', 'eu', 'hm', 'ic', 'sh', 'sj', 'ta', 'un',
        'xk', 'bv', 'aq', 'tf', 'gs', 'io', 'um',
    ])

    const characters = Object.entries(codes)
        .filter(([code]) => code.length === 2 && !exclude.has(code))
        .map(([code, name]) => ({
            name,
            imageUrl: `https://flagcdn.com/w1280/${code}.webp`,
        }))
        .sort((a, b) => a.name.localeCompare(b.name))

    console.log(`Flags: ${characters.length} countries`)
    return (
        header + `export const flagCharacters: Character[] = ${serialize(characters)}\n`
    )
}

async function fetchPokemon(): Promise<string> {
    const data = await fetch(
        'https://pokeapi.co/api/v2/pokemon?limit=1025',
    ).then((r) => r.json())

    const characters = (data.results as { name: string; url: string }[])
        .slice(0, 151)
        .map(({ name, url }) => {
            const id = url.split('/').filter(Boolean).pop()!
            return {
                name: name.charAt(0).toUpperCase() + name.slice(1).replace(/-/g, ' '),
                imageUrl: `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/other/official-artwork/${id}.png`,
            }
        })

    console.log(`Pokémon: ${characters.length} pokémon`)
    return (
        header + `export const pokemonCharacters: Character[] = ${serialize(characters)}\n`
    )
}

