async function main() {
    const tmdbApiKey = process.env.TMDB_API_KEY
    if (!tmdbApiKey) throw new Error('TMDB_API_KEY env variable is required')

    console.log('Generating character data...\n')

    const [lol, dota2, valorant, flags, pokemon, fortnite, genshin, rickAndMorty, overwatch, dragonBall, naruto, movies, tvShows, people] =
        await Promise.all([
            fetchLol(),
            fetchDota2(),
            fetchValorant(),
            fetchFlags(),
            fetchPokemon(),
            fetchFortnite(),
            fetchGenshin(),
            fetchRickAndMorty(),
            fetchOverwatch(),
            fetchDragonBall(),
            fetchNaruto(),
            fetchMovies(tmdbApiKey),
            fetchTvShows(tmdbApiKey),
            fetchPeople(tmdbApiKey),
        ])

    // Jikan API has rate limiting, fetch sequentially
    const onePiece = await fetchOnePiece()
    await new Promise((r) => setTimeout(r, 1000))
    const attackOnTitan = await fetchAttackOnTitan()
    await new Promise((r) => setTimeout(r, 1000))
    const demonSlayer = await fetchDemonSlayer()

    writeFileSync(join(OUT_DIR, 'league-of-legends.ts'), lol)
    writeFileSync(join(OUT_DIR, 'dota-2.ts'), dota2)
    writeFileSync(join(OUT_DIR, 'valorant.ts'), valorant)
    writeFileSync(join(OUT_DIR, 'flags.ts'), flags)
    writeFileSync(join(OUT_DIR, 'pokemon.ts'), pokemon)
    writeFileSync(join(OUT_DIR, 'fortnite.ts'), fortnite)
    writeFileSync(join(OUT_DIR, 'genshin.ts'), genshin)
    writeFileSync(join(OUT_DIR, 'rick-and-morty.ts'), rickAndMorty)
    writeFileSync(join(OUT_DIR, 'overwatch.ts'), overwatch)
    writeFileSync(join(OUT_DIR, 'dragon-ball.ts'), dragonBall)
    writeFileSync(join(OUT_DIR, 'naruto.ts'), naruto)
    writeFileSync(join(OUT_DIR, 'movies.ts'), movies)
    writeFileSync(join(OUT_DIR, 'tv-shows.ts'), tvShows)
    writeFileSync(join(OUT_DIR, 'people.ts'), people)
    writeFileSync(join(OUT_DIR, 'one-piece.ts'), onePiece)
    writeFileSync(join(OUT_DIR, 'attack-on-titan.ts'), attackOnTitan)
    writeFileSync(join(OUT_DIR, 'demon-slayer.ts'), demonSlayer)

    console.log('\nDone! Files written to src/data/characters/')
}

main().catch((err) => {
    console.error(err)
    process.exit(1)
})
